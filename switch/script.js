var number = parseInt(prompt("Chute um número, qualquer número"))

switch (number) {
    case 1:
        document.write("Você escreveu " + "1")
    break

    case 2:
        document.write("Você escreveu " + "2")
    break

    case 3:
        document.write("Você escreveu " + "3")
    break

    case 4:
        document.write("Você escreveu " + "4")
    break

    case 5:
        document.write("Você escreveu " + "5")
    break

    default:
        document.write("Vazio")
    break
} 
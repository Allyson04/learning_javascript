let fruits = []

fruits[0] = "apple"
fruits[1] = "pear"

//adding an element to the end of an array
fruits.push("watermelon")

//adding an elements to the beginning of an array
fruits.unshift("grape")

// excluding an element in the end of an array
fruits.pop()

//excluding an elements at the start of an array
fruits.shift()
console.log(fruits)
